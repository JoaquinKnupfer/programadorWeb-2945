var firstNumber = prompt ("Ingrese un numero")

var secondNumber = prompt ("Ingrese otro numero")

var parsedFirstNumber = parseInt (firstNumber, 10)

var parsedSecondNumber = parseInt (secondNumber, 10)

console.log ('La suma es ' + (parsedFirstNumber + parsedSecondNumber))

console.log ('La resta es ' + (parsedFirstNumber - parsedSecondNumber))

console.log ('La multipliacion es ' + (parsedFirstNumber * parsedSecondNumber))

console.log ('La division es ' + (parsedFirstNumber / parsedSecondNumber))
